#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}
int 	main(void)
{
	char dest[] = "Hello";
	char src[] = "World";
	ft_strcpy(dest, src);
	printf("%s\n", dest);
	return (0);
}